package ClaculatorAppPackage;

public class Maths {

	
	
	public void add(int a, int b) {
		int number1 = a;
		int number2 = b;
		int addition = number1+number2;
		System.out.println("Addition of given two number is :" +addition+"\n");
	}
	public void substract(int a, int b) {
		int number1 = a;
		int number2 = b;
		int substraction = number1-number2;
		System.out.println("Substraction of given two number is :"+substraction+"\n");
	}
	
	public void multiply(int a, int b) {
		int number1 = a;
		int number2 = b;
		int multiply = number1*number2;
		System.out.println("Multiply of given two number is :"+multiply+"\n");
	}

	public void div(int a, int b) {
		int number1 = a;
		int number2 = b;
		int division = number1/number2;
		System.out.println("Division of given two number is :"+division+"\n");
	}

//
}
